#include <iostream>
using namespace std;

int main() {
    int a = 3;
    int b = 4;

    cout << (a > b) << endl;    // output of relational operator in form of ( 0 and 1 ); 
    cout << (a < b) << endl;                            // 0 = false;  and  1 = true;
    cout << (a >= b) << endl;
    cout << (a <= b) << endl;
    cout << (a == b) << endl;
    cout << (a != b) << endl;

    return 0;
}
 